"""
Kite Logik — governance middleware for enterprise AI agents.

Prerequisite — an OPA engine
----------------------------
``OPAClient`` (default) expects an Open Policy Agent server reachable at
``http://localhost:8181``. The quickest path is::

    docker run -d --name opa -p 8181:8181 -v $(pwd)/policies:/policies \\
        openpolicyagent/opa:latest run --server /policies

Policy files are generated by ``kitelogik init <project>``. For a pure-Python
alternative that avoids the Docker dependency, install the ``regorus`` extra
and use ``RegorusClient`` in place of ``OPAClient``.

Quickstart::

    from kitelogik import AgentSession, OPAClient, PolicyGate, HITLQueue, SessionContext

    opa    = OPAClient()
    gate   = PolicyGate(opa_client=opa)
    queue  = HITLQueue()
    context = SessionContext(session_id="s1", user_role="support_agent",
                             session_scopes=["read_customer"])
    session = AgentSession(gate=gate, context=context, hitl_queue=queue)
    result  = await session.run_async("Look up customer cust_001")

See quickstart.py for a complete working example.
"""

